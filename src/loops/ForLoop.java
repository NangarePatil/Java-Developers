package loops;

public class ForLoop

{

	public static void main(String[] args) 
	
	{
		//write table of two
		//start -->2 condition--><=20 updation-->i=i+2
		for(int i=2;i<=20;i=i+2)//2,4,6
		{
		System.out.println(i);
		}
		//=======================================================
		System.out.println("============");
		//write table of two in reverse order
		//start-->20 condition--> >=2 updation-->i=i-2
		for(int i=20;i>=2;i=i-2)
		{
		System.out.println(i);
		}

	}

}
