package loops;

public class WhileLoop 

{

	public static void main(String[] args) 
	
	{
		//I want print Pune 20 times
		//start-->1 condition --> <=20 updation-->i++
		int i=1;
		while(i<=20)
		{
		System.out.println("Pune");
		i++;
		}
		//=================================
		System.out.println("=====================");
		// I want to print Katraj 15 times
		//start-->15 condition --> >=1 updation-->i--
		int j=15;
		while(j>=1)
		{
		System.out.println("Katraj");
		j--;
		}


	}

}
