package loops;

public class DoWhileLoop 

{

	public static void main(String[] args) 
	
	
	{
		//I want to print India 5 times
		//start--> 1 condition--> <=5 update--> i++
		int i=1;
		do
		{
		System.out.println("India");
		i++;//2,3,4,5,6
		}
		while(i<=5);
		System.out.println("=================");
		//=======================================
		//I want to print MH 10 times
		//start--> 10 condition--> >=1 update--> i--
		int j=10;
		do
		{
		System.out.println("MH");
		j--;
		}
		while(j>=1);
		}

	}


